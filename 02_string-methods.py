# Python 2

# Capitalize

Cap_sentence = "py python"
capitalized_sentence = Cap_sentence.capitalize()
print(capitalized_sentence)

# Lower Case
Lower_sentence = "pY pYtHon"
lowerCase_sentence = Lower_sentence.casefold()
print(lowerCase_sentence)

# Center
center_sentence = "Python Center"
center_text = center_sentence.center(24, '*')
print(center_text)

# Count
count_text = center_sentence.count('P')
print(count_text)